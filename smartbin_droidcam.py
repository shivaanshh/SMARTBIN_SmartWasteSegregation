from ultralytics import YOLO
import cv2
import numpy as np
import random

# --- Load your best model ---
model = YOLO("/content/best.pt")  # <-- correct path

# --- 🔹 CHANGE THIS LINE for DROIDCAM feed ---
# Open DroidCam stream (make sure phone and PC are on same Wi-Fi)
# Example default port: 4747 (check DroidCam app for your IP)
SOURCE = "http://10.134.205.107:4747/video"  # Updated to provided IP

cap = cv2.VideoCapture(SOURCE)
if not cap.isOpened():
    raise SystemExit("❌ Could not connect to DroidCam. Check IP or app.")

print("✅ SmartBin YOLOv8 Real-Time Detection (DroidCam) Started")
print("Press 'Q' to quit.")

# --- Assign color per class for consistent visualization ---
colors = {name: [random.randint(0, 255) for _ in range(3)] for name in model.names.values()}

CONF_THRES = 0.55  # stricter = cleaner detections
IOU_THRES  = 0.4
IMGSZ = 640

while True:
    ret, frame = cap.read()
    if not ret:
        print("⚠️ Frame not received. Check DroidCam connection.")
        break

    # --------------- 🧠 ASPECT-RATIO PRESERVING PREPROCESSING ---------------
    h, w = frame.shape[:2]
    scale = IMGSZ / max(h, w)
    resized = cv2.resize(frame, (int(w * scale), int(h * scale)))
    top = (IMGSZ - resized.shape[0]) // 2
    bottom = IMGSZ - resized.shape[0] - top
    left = (IMGSZ - resized.shape[1]) // 2
    right = IMGSZ - resized.shape[1] - left
    padded = cv2.copyMakeBorder(resized, top, bottom, left, right, cv2.BORDER_CONSTANT, value=(114, 114, 114))
    # -----------------------------------------------------------------------

    # Run YOLOv8 inference
    results = model.predict(
        source=padded,
        conf=CONF_THRES,
        iou=IOU_THRES,
        imgsz=IMGSZ,
        verbose=False
    )

    # Draw precise bounding boxes
    for box in results[0].boxes:
        x1, y1, x2, y2 = map(int, box.xyxy[0])
        cls = int(box.cls[0])
        conf = float(box.conf[0])
        label = f"{model.names[cls]} {conf:.2f}"
        color = colors[model.names[cls]]

        cv2.rectangle(padded, (x1, y1), (x2, y2), color, 2)
        cv2.putText(padded, label, (x1, y1 - 10),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.7, color, 2, cv2.LINE_AA)

    cv2.imshow("♻️ SmartBin YOLOv8 – Precise Detection (DroidCam)", padded)

    # Quit on 'Q'
    if cv2.waitKey(1) & 0xFF == ord('q'):
        break

cap.release()
cv2.destroyAllWindows()
print("👋 Stream closed.")

