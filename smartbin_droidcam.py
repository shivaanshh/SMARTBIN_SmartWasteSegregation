from ultralytics import YOLO
import cv2
import random
import os
from IPython.display import Image, display

# --- 1️⃣ Load your best trained model ---
model = YOLO('/content/best.pt')

# --- 2️⃣ Assign distinct colors for each waste class ---
colors = {name: [random.randint(0, 255) for _ in range(3)]
          for name in model.names.values()}

# --- 3️⃣ Path to your test image folder ---
test_dir = '/content/data/test/images'  # <-- change if your test folder differs

# --- 4️⃣ Create folder for saving annotated images ---
os.makedirs('annotated_results', exist_ok=True)

# --- 5️⃣ Loop through test images for detection ---
for img_file in os.listdir(test_dir):
    img_path = os.path.join(test_dir, img_file)
    if not img_path.lower().endswith(('.jpg', '.png', '.jpeg')):
        continue

    # Run inference
    results = model(img_path, conf=0.5, iou=0.45, imgsz=640, verbose=False)

    # Get original image
    frame = results[0].orig_img.copy()

    # Draw refined bounding boxes manually
    for box in results[0].boxes:
        # Extract box coordinates, class, and confidence
        x1, y1, x2, y2 = map(int, box.xyxy[0])
        cls = int(box.cls[0])
        conf = float(box.conf[0])
        label = f"{model.names[cls]} {conf:.2f}"

        # Use a unique color for each class
        color = colors[model.names[cls]]

        # Draw precise rectangle with smooth corners
        cv2.rectangle(frame, (x1, y1), (x2, y2), color, 3)
        cv2.putText(frame, label, (x1, y1 - 10),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.8, color, 2, cv2.LINE_AA)

    # Save the annotated image
    save_path = f"annotated_results/{img_file}"
    cv2.imwrite(save_path, frame)

    # Display inside Colab (preview only first few)
    display(Image(filename=save_path))

